const mainTable = document.querySelector('table');
const navContainer = document.querySelector('.list-nav');

const clip = () => {
    navigator.clipboard
        .writeText(document.querySelector('ul').innerText)
        .then(() => {
            alert('List successfully copied to clipboard!');
        });
};

const renderList = (letter) => {
    if (nameObj[letter]) {
        mainTable.innerHTML = '';
        nameObj[letter].forEach((name, index) => {
            const row = document.createElement('tr');
            const entry = document.createElement('td');
            entry.innerText = name.name;
            entry.title = 'click to like';
            entry.onclick = () => {
                if (
                    !document
                        .getElementById('liked-list')
                        .innerText.includes(entry.innerText)
                ) {
                    let newLiked = document.createElement('li');
                    newLiked.id = entry.innerText;
                    newLiked.innerText = entry.innerText;
                    document.getElementById('liked-list').appendChild(newLiked);
                }
            };
            row.appendChild(entry);
            mainTable.appendChild(row);
        });
    }
};

remainingLetters.split('').forEach((letter) => {
    const showList = document.createElement('a');
    showList.innerText = letter.toUpperCase();
    showList.onclick = () => renderList(letter);
    navContainer.appendChild(showList);
});
